
public class Book1 {
	private String Name;
	private int price;
	public Book1(String name, int price) {
		super();
		Name = name;
		this.price = price;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	

}
